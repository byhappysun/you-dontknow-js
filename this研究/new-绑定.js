/*
首先我们重新定义一下 JavaScript 中的“ 构造函数”。 
在 JavaScript 中, 构造函数只是一些 使用 new 操作符时被调用的函数。 
它们并不会属于某个类, 
也不会实例化一个类。 
实际上, 它们甚至都不能说是一种特殊的函数类型, 它们只是被 new 操作符调用的普通函数而已。
*/
/*细微的区别
这里有一个重要但是非常细微的区别: 
实际上并不存在所谓的“构造函数”, 
只有对于函数的“ 构造调用”。
*/
/**
 * 
 * @param {
 1. 创建(或者说构造) 一个全新的对象。
 2. 这个新对象会被执行[[原型]] 连接。
 3. 这个新对象会绑定到函数调用的this。
 4. 如果函数没有返回其他对象, 那么new表达式中的函数调用会自动返回这个新对象。
 * }
 */
function hunsha(sha) {
    this.sha = sha;
}
var mysha = new hunsha('lf-hunsha');
console.log(mysha.sha);